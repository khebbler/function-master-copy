// Function 10 - Has Words ///////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function hasWord(string, word) {
    // Splitting string into an array
    var wordArray = string.split(' ');
    // Checking if word is in array
    return wordArray.includes(word);
}

